package cast.cruise.domain;
/**
* Οι διαφορετικές καταστάσεις μιας κρουαζιέρας.
* 
*/

public enum CruiseState {

	
	/**
     * Η κρουαζιέρα είναι διαθέσιμη.
     */
    
    AVAILABLE,
    /**
     * Η κρουαζιέρα έχει ολοκληρωθεί.
     */
	FINISH,
	/**
     * Η κρουαζιέρα έχει αρχίσει.
     */
    START,
    /**
     * Οι θέσεις για την κρουαζιέρα είναι γεμάτες.
     */
    FULL
}